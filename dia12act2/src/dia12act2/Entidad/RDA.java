package dia12act2.Entidad;

public class RDA {
    
    private int posact;
    private int posagu;

    public RDA() {
    }

    public RDA(int posact, int posagu) {
        this.posact = posact;
        this.posagu = posagu;
    }

    public int getPosact() {
        return posact;
    }

    public void setPosact(int posact) {
        this.posact = posact;
    }

    public int getPosagu() {
        return posagu;
    }

    public void setPosagu(int posagu) {
        this.posagu = posagu;
    }
    
    
}
